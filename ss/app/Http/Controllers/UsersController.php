    {
        $username= $request->name;
        $password= $request->password;
        $user= User::where('name',$username)->get();
        if ($user->isEmpty()) { return response()->json(['code'=>501,'msg'=>'用户名不存在']);}
        $a = $user ->toArray();
         if ($a[0]['password']==$password){return response()->json(['code'=>200,'msg'=>'登录成功','user'=>$a[0]['name']]);}
         return response()->json(['code'=>300,'msg'=>'密码错误',]);
    }

